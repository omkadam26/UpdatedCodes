public class ForDemo {
    public static void main(String[] args) {
        for(int i=1;i<=10;)
        {
            System.out.println("    My Number is :    "+i);
            i+=1;
        }
        System.out.println("=======================================");
        int j=1;
        while(j<=5)
        {
            System.out.println("My name is : "+j);
            j+=1;
        }
        System.out.println("===============================================");
        int k=1;
        do
        {
            System.out.println("Do while Demo ");
        }while(k<=10);
        
        
    }
}
